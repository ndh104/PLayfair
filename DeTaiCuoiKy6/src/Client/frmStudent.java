    private void btnSaveAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAddActionPerformed
        // TODO add your handling code here:
        int out = JOptionPane.showConfirmDialog(null, "Do you want to save this Student?", "SAVE?", JOptionPane.OK_CANCEL_OPTION);
        if (out == 0) {
            try {
                String studebtID = txtStudentID.getText();
                String fullName = txtFullName.getText();
                String sex = txtSex.getText();
                String phoneNumber = txtPhoneNumber.getText();
                String mail = txtMail.getText();
                String address = txtAddress.getText();
                boolean check = DBAccess.addStudent(studebtID, fullName, sex, phoneNumber, mail, address);
                DefaultTableModel tbModel = (DefaultTableModel) tb_Student.getModel();
                tbModel.setRowCount(0);
                show_Student();
                btnSaveAdd.setVisible(false);
                btnCancelAdd.setVisible(false);
                btnAdd.setVisible(true);
                btnEdit.setVisible(true);
                if (check) {
                    JOptionPane.showMessageDialog(null, "Saved!");
                } else {
                    JOptionPane.showMessageDialog(null, "This Student is already in database!");
                }
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveAddActionPerformed

