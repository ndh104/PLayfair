    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        int out = JOptionPane.showConfirmDialog(null, "Do you want to delete this Student?", "DELETE?", JOptionPane.OK_CANCEL_OPTION);
        if (out == 0) {
            try {
                int row = tb_Student.getSelectedRow();
                String studentID = (tb_Student.getModel().getValueAt(row, 0).toString());
                boolean check = DBAccess.deleteStudent(studentID);
                DefaultTableModel tbModel = (DefaultTableModel) tb_Student.getModel();
                tbModel.setRowCount(0);
                show_Student();
                txtStudentID.setText("");
                txtFullName.setText("");
                txtSex.setText("");
                txtPhoneNumber.setText("");
                txtMail.setText("");
                txtAddress.setText("");
                if (check) {
                    JOptionPane.showMessageDialog(null, "Deleted!");
                } else {
                    JOptionPane.showMessageDialog(null, "This Student being used!");
                }
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

