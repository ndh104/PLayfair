    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        int out = JOptionPane.showConfirmDialog(null, "Do you want to delete this Subject?", "DELETE?", JOptionPane.OK_CANCEL_OPTION);
        if (out == 0) {
            try {
                int row = tb_Subject.getSelectedRow();
                String subID = (tb_Subject.getModel().getValueAt(row, 0).toString());
                DBAccess.deleteSubject(subID);
                DefaultTableModel tbModel = (DefaultTableModel) tb_Subject.getModel();
                tbModel.setRowCount(0);
                show_Subject();
                txtSubID.setText("");
                txtSubName.setText("");
                txtSubCre.setText("");
                JOptionPane.showMessageDialog(null, "Deleted!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

