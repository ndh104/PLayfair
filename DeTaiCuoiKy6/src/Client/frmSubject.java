    private void tb_SubjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_SubjectMouseClicked
        // TODO add your handling code here:
        int row = tb_Subject.getSelectedRow();
        if (row >= 0) {
            txtSubID.setText(tb_Subject.getValueAt(row, 0).toString());
            txtSubName.setText(tb_Subject.getValueAt(row, 1).toString());
            txtSubCre.setText(tb_Subject.getValueAt(row, 2).toString());
        }
    }//GEN-LAST:event_tb_SubjectMouseClicked

    private void btnSaveAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAddActionPerformed
        // TODO add your handling code here:
        int out = JOptionPane.showConfirmDialog(null, "Do you want to save this Subject?", "SAVE?", JOptionPane.OK_CANCEL_OPTION);
        if (out == 0) {
            try {
                String subID = txtSubID.getText();
                String subName = txtSubName.getText();
                int subCredits = Integer.valueOf(txtSubCre.getText());
                DBAccess.addSubject(subID, subName, subCredits);
                DefaultTableModel tbModel = (DefaultTableModel) tb_Subject.getModel();
                tbModel.setRowCount(0);
                show_Subject();
                btnSaveAdd.setVisible(false);
                btnCancelAdd.setVisible(false);
                btnAdd.setVisible(true);
                btnEdit.setVisible(true);
                JOptionPane.showMessageDialog(null, "Saved!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveAddActionPerformed

