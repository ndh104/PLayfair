    public void show_Subject() {
        List<Subject> subjectlist = DBAccess.getAllSubject();
        DefaultTableModel model = (DefaultTableModel) tb_Subject.getModel();
        Object[] row = new Object[10];
        for (int i = 0; i < subjectlist.size(); i++) {
            row[0] = subjectlist.get(i).getSubID();
            row[1] = subjectlist.get(i).getSubName();
            row[2] = subjectlist.get(i).getSubCredits();
            model.addRow(row);
        }
    }

