    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int out = JOptionPane.showConfirmDialog(null, "Do you want to save this Teacher?", "SAVE?", JOptionPane.OK_CANCEL_OPTION);
        if (out == 0) {
            try {
                String teacherID = txtTeacherID.getText();
                String userName = txtUsername.getText();
                String fullName = txtFullName.getText();
                String sex = txtSex.getText();
                String phoneNumber = txtPhoneNumber.getText();
                String mail = txtMail.getText();
                String address = txtAddress.getText();
                DBAccess.addTeacher(teacherID, userName, fullName, sex, phoneNumber, mail, address);
                DefaultTableModel tbModel = (DefaultTableModel) tb_Teacher.getModel();
                tbModel.setRowCount(0);
                show_Teacher();
                btnSave.setVisible(false);
                btnCancel.setVisible(false);
                btnAdd.setVisible(true);
                btnEdit.setVisible(true);
                JOptionPane.showMessageDialog(null, "Saved!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        int out = JOptionPane.showConfirmDialog(null, "Do you want to delete this Teacher?", "DELETE?", JOptionPane.OK_CANCEL_OPTION);
        if (out == 0) {
            try {
                int row = tb_Teacher.getSelectedRow();
                String teacherID = (tb_Teacher.getModel().getValueAt(row, 0).toString());
                DBAccess.deleteTeacher(teacherID);
                DefaultTableModel tbModel = (DefaultTableModel) tb_Teacher.getModel();
                tbModel.setRowCount(0);
                show_Teacher();
                JOptionPane.showMessageDialog(null, "Deleted!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

