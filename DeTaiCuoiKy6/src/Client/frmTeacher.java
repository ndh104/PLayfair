    public void show_Teacher() {
        List<Teacher> teacherlist = DBAccess.getAllTeacher();
        DefaultTableModel model = (DefaultTableModel) tb_Teacher.getModel();
        Object[] row = new Object[10];
        for (int i = 0; i < teacherlist.size(); i++) {
            row[0] = teacherlist.get(i).getTeacherID();
            row[1] = teacherlist.get(i).getUserName();
            row[2] = teacherlist.get(i).getFullName();
            row[3] = teacherlist.get(i).getSex();
            row[4] = teacherlist.get(i).getPhoneNumber();
            row[5] = teacherlist.get(i).getMail();
            row[6] = teacherlist.get(i).getAddress();
            model.addRow(row);
        }
    }

